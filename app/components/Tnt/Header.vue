<script setup lang="ts">
import { tv } from 'tailwind-variants'

const localePath = useLocalePath()

const { locale, defaultLocale } = useI18n()

const {
  site: { name },
  content: {
    i18n: contentI18n
  },
  header: {
    navigation: { content: navContent, links: navLinks },
  },
} = useAppConfig() as any

// @i18n

const { /* color = 'neutral', */ variant = undefined } = defineProps<{
  // color?: 'neutral' | 'primary'
  variant?: 'bordered' | 'solid' | 'ghost'
  // TODO: Size - it would be nice to allow larger text; given that this
  //       ought to be a fixed/sticky element though, we must give consideration
  //       to the space occupied when positioning other content.
}>()

// NOTE: useAsyncData removed
const navItems = await tntNav(navContent ? navContent : navLinks, undefined, { locale: contentI18n ? (locale.value === defaultLocale ? true : locale.value) : false })

const navbar = computed(() => tv({
  base: 'w-full',
  variants: {
    variant: {
      bordered: 'bg-muted border-b border-accented',
      solid: 'bg-(--ui-bg)',
      ghost: 'bg-transparent',
    },
  },
  compoundVariants: [
    {
      variant: ['solid', 'ghost'],
      class: 'border-none',
    },
  ],
  defaultVariants: {
    variant: 'bordered',
  },
}))
</script>

<template lang="pug">
div(:class="navbar({ variant })")
  UContainer(
    class="flex \
          items-center \
          justify-between"
  )
    div(class="flex")
      TntNavigationDrawer(:items="navItems")/

      UButton(
        :label="name"
        :to="localePath('/')"
        color="neutral"
        variant="ghost"
        size="xl"
        class="my-2 px-2.5 py-1.5 font-extrabold"
      )/

    div(class="flex")
      TntSearch/
      TntColorMode/
</template>
